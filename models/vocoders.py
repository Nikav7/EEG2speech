from torch.nn.utils.parametrizations import weight_norm

LRELU_SLOPE = 0.1

def remove_weight_norm(module: nn.Module) -> None:
    """Backward-compatible remover for parametrizations.weight_norm."""
    try:
        parametrize.remove_parametrizations(module, "weight", leave_parametrized=False)
    except Exception:
        pass

class TorchaudioHiFiGAN16k(nn.Module):
    """Wrapper around SpeechBrain HiFi-GAN LibriTTS vocoder (16 kHz).

    Accepts log-mel power-dB spectrograms produced with:
        sr=16000, n_fft=1024, hop_length=256, n_mels=80, fmin=20, fmax=8000
    and returns 16 kHz waveforms of shape [B, 1, T].
    """

    SAMPLE_RATE = 16000

    def __init__(self, device: torch.device | None = None):
        super().__init__()
        # Lazy import so optional speechbrain integrations (e.g., k2) are not loaded
        # unless this specific backend is selected.
        try:
            from speechbrain.inference.vocoders import HIFIGAN
            from speechbrain.utils.fetching import LocalStrategy
        except Exception as exc:
            raise ImportError(
                "SpeechBrain HiFi-GAN backend failed to import. "
                "Install/verify speechbrain or use --vocoder_type griffinlim."
            ) from exc

        if device is None:
            sb_device = "cpu"
        else:
            # SpeechBrain expects explicit CUDA index string, e.g. "cuda:0".
            if device.type == "cuda":
                idx = 0 if device.index is None else int(device.index)
                sb_device = f"cuda:{idx}"
            else:
                sb_device = "cpu"

        self._vocoder = HIFIGAN.from_hparams(
            source="speechbrain/tts-hifigan-libritts-16kHz",
            savedir="pretrained_models/tts-hifigan-libritts-16kHz",
            run_opts={"device": sb_device},
            local_strategy=LocalStrategy.COPY,
        )
        self._vocoder.eval()
        for p in self._vocoder.parameters():
            p.requires_grad = False


    def forward(self, mel: torch.Tensor) -> torch.Tensor:
        """mel: [B, n_mels, T] power-dB  ->  wav: [B, 1, T_wav]"""
        mel_in = mel.float()
        # hifi16KHz expects linear-amplitude mels (not log-dB), we convert from power-dB → power → linear amplitude before passing to the model.
        # Convert power-dB back to power then to linear amplitude
        mel_linear = torchaudio.functional.DB_to_amplitude(mel_in, ref=1.0, power=1.0).clamp_min(1e-8)

        # Keep vocoder frozen, but do NOT use decode_batch() because it wraps no_grad.
        gen = self._vocoder.hparams.generator
        if getattr(self._vocoder, "first_call", False):
            gen.remove_weight_norm()
            self._vocoder.first_call = False

        pad = int(getattr(gen, "inference_padding", 0))
        if pad > 0:
            mel_linear = F.pad(mel_linear, (pad, pad), "replicate")

        vocoder_device = getattr(self._vocoder, "device", mel_linear.device)
        wav = gen(mel_linear.to(vocoder_device))
        return wav


class GriffinLimVocoder(nn.Module):
            def __init__(self, n_mels: int, sample_rate: int):
                super().__init__()
                n_fft = 1024
                n_stft = (n_fft // 2) + 1
                hop_length = 512
                win_length = 1024
                self.inv_mel = torchaudio.transforms.InverseMelScale(
                    n_stft=n_stft,
                    n_mels=int(n_mels),
                    sample_rate=int(sample_rate),
                )
                self.griffin = torchaudio.transforms.GriffinLim(
                    n_fft=n_fft,
                    n_iter=32,
                    win_length=win_length,
                    hop_length=hop_length,
                    power=2.0,
                )

            def forward(self, mel: torch.Tensor) -> torch.Tensor:
                mel_in = mel.float()
                # Input mels are power-dB (librosa.power_to_db). Invert back to power with DB_to_amplitude
                mel_linear = torchaudio.functional.DB_to_amplitude(mel_in, ref=1.0, power=1.0).clamp_min(1e-8)
                spec = self.inv_mel(mel_linear).clamp_min(1e-8)
                wav = self.griffin(spec)
                return wav.unsqueeze(1)

